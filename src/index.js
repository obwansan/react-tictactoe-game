import React from 'react';
import ReactDOM from 'react-dom';
import './index.css';

function Square(props) {
    return (
      <button className="square" onClick={props.onClick}>
        {props.value}
      </button>
    );
}

class Board extends React.Component {
  constructor(props) {
    super(props);
    this.state = {
      squares: Array(9).fill(null),
      xIsNext: true,
    }
  }

  handleClick(i) {
    // Get a copy of all the squares (their values)
    const squares = this.state.squares.slice();
    // If calculateWinner returns a value (X or O, not null), i.e. if someone has won,
    // or the clicked square has a value, don't do anything.
    if (calculateWinner(squares) || squares[i]) {
      return;
    }
    // If there's no winner and the square is empty, set the value of the
    // clicked square to X if xIsNext is true, or O if it's not.
    squares[i] = this.state.xIsNext ? 'X' : 'O';

    // Update values of squares stored in state.
    // Toggle boolean value of xIsNext so that the next time handleClick() is
    // called the square will be assigned a O if the previous go was an X and
    // vica versa.
    this.setState({
      squares: squares,
      xIsNext: !this.state.xIsNext,
    });
  }

  // value is null for all squares initially (because 9 null values in the
  // squares array on state object)
  renderSquare(i) {
    return (
      <Square
        value={this.state.squares[i]}
        onClick={() => this.handleClick(i)}
      />
    );
  }

  render() {
    const winner = calculateWinner(this.state.squares);
    let status;
    if (winner) {
      status = 'Winner: ' + winner;
    } else {
      status = 'Next player: ' + (this.state.xIsNext ? 'X' : 'O');
    }

    return (
      <div>
        <div className="status">{status}</div>
        <div className="board-row">
          {this.renderSquare(0)}
          {this.renderSquare(1)}
          {this.renderSquare(2)}
        </div>
        <div className="board-row">
          {this.renderSquare(3)}
          {this.renderSquare(4)}
          {this.renderSquare(5)}
        </div>
        <div className="board-row">
          {this.renderSquare(6)}
          {this.renderSquare(7)}
          {this.renderSquare(8)}
        </div>
      </div>
    );
  }
}

class Game extends React.Component {
  render() {
    return (
      <div className="game">
        <div className="game-board">
          <Board />
        </div>
        <div className="game-info">
          <div>{/* status */}</div>
          <ol>{/* TODO */}</ol>
        </div>
      </div>
    );
  }
}

// ========================================

ReactDOM.render(
  <Game />,
  document.getElementById('root')
);

// squares is the array of Xs and Os comprising the board at any given time
function calculateWinner(squares) {
  // Horizontal, vertical and diagonal winning lines
  const lines =[
    [0, 1, 2],
    [3, 4, 5],
    [6, 7, 8],
    [0, 3, 6],
    [1, 4, 7],
    [2, 5, 8],
    [0, 4, 8],
    [2, 4, 6],
  ];
  for (let i = 0; i < lines.length; i++) {
    const [a, b, c] = lines[i];
    if (squares[a] && squares[a] === squares[b] && squares[a] === squares[c]) {
      return squares[a];
    }
  }
  return null;
}
